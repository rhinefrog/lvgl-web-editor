/* lv_port_disp.c                                                */
/* ============================================================ */
function generatePortDisp(board: BoardDefinition): string {
  const res = board.resolution;
  const d = board.display;
  const W = res.w;
  const H = res.h;

  let displayInitBlock: string;
  let flushBlock: string;

  if (d.controller === 'ST7262') {
    displayInitBlock = [
      '    pio_rgb_info_t rgb_info;',
     `    rgb_info.width = ${W};`,
     `    rgb_info.height = ${H};`,
     `    rgb_info.transfer_size = ${d.transferSize || `${W} * 80`};`,
     `    rgb_info.pclk_freq = ${d.pclkFreq || 32000000};`,
     `    rgb_info.mode.double_buffer = ${d.doubleBuffer ? 'true' : 'false'};`,
      '    rgb_info.mode.enabled_transfer = true;',
     `    rgb_info.mode.enabled_psram = ${d.psram ? 'true' : 'false'};`,
     `    rgb_info.framebuffer1 = malloc(${W} * ${H} * sizeof(uint16_t));`,
      '    rgb_info.dma_flush_done_cb = disp_flush_done;',
      '',
      '    bsp_display_info_t display_info;',
     `    display_info.width = ${W};`,
     `    display_info.height = ${H};`,
      '    display_info.brightness = 80;',
      '    display_info.dma_flush_done_cb = disp_flush_done;',
      '    display_info.user_data = &rgb_info;',
      '',
      '    bsp_display_new_st7262(&display_if, &display_info);',
      '    display_if->init();',
    ].join('\n');

    flushBlock = [
      'static void disp_flush(lv_disp_drv_t *drv, const lv_area_t *area, lv_color_t *color_p) {',
      '    bsp_display_interface_t *di = (bsp_display_interface_t *)drv->user_data;',
      '    bsp_display_area_t da = { .x1 = area->x1, .x2 = area->x2, .y1 = area->y1, .y2 = area->y2 };',
      '    di->flush_dma(&da, color_p);',
      '}',
      '',
      'static void disp_flush_done(void) { lv_disp_flush_ready(&disp_drv); }',
    ].join('\n');
  } else if (d.controller === 'ST7701S') {
    displayInitBlock = [
      '    pio_rgb_info_t rgb_info;',
     `    rgb_info.width = ${W};`,
     `    rgb_info.height = ${H};`,
     `    rgb_info.transfer_size = ${d.transferSize || `${W} * 40`};`,
     `    rgb_info.pclk_freq = ${d.pclkFreq || 20000000};`,
     `    rgb_info.mode.double_buffer = ${d.doubleBuffer ? 'true' : 'false'};`,
      '    rgb_info.mode.enabled_transfer = true;',
     `    rgb_info.mode.enabled_psram = ${d.psram ? 'true' : 'false'};`,
     `    rgb_info.framebuffer1 = malloc(${W} * ${H} * sizeof(uint16_t));`,
      '    rgb_info.dma_flush_done_cb = NULL;',
      '',
      '    bsp_display_info_t display_info;',
     `    display_info.width = ${W};`,
     `    display_info.height = ${H};`,
      '    display_info.brightness = 80;',
      '    display_info.dma_flush_done_cb = NULL;',
      '    display_info.user_data = &rgb_info;',
      '',
      '    bsp_display_new_st7701s(&display_if, &display_info);',
      '    display_if->init();',
    ].join('\n');

    flushBlock = [
      'static void disp_flush(lv_disp_drv_t *drv, const lv_area_t *area, lv_color_t *color_p) {',
      '    bsp_display_interface_t *di = (bsp_display_interface_t *)drv->user_data;',
      '    bsp_display_area_t da = { .x1 = area->x1, .x2 = area->x2, .y1 = area->y1, .y2 = area->y2 };',
      '    di->flush_dma(&da, color_p);',
      '    lv_disp_flush_ready(drv);',
      '}',
    ].join('\n');
  } else if (d.controller === 'ST7701') {
    displayInitBlock = [
      '    pio_rgb_info_t rgb_info;',
     `    rgb_info.width = ${W};`,
     `    rgb_info.height = ${H};`,
     `    rgb_info.transfer_size = ${d.transferSize || `${W} * 240`};`,
     `    rgb_info.pclk_freq = ${d.pclkFreq || 20000000};`,
     `    rgb_info.mode.double_buffer = ${d.doubleBuffer ? 'true' : 'false'};`,
      '    rgb_info.mode.enabled_transfer = true;',
     `    rgb_info.mode.enabled_psram = ${d.psram ? 'true' : 'false'};`,
     `    rgb_info.framebuffer1 = malloc(${W} * ${H} * sizeof(uint16_t));`,
      '    rgb_info.dma_flush_done_cb = NULL;',
      '',
      '    bsp_display_info_t display_info;',
     `    display_info.width = ${W};`,
     `    display_info.height = ${H};`,
      '    display_info.brightness = 80;',
      '    display_info.dma_flush_done_cb = NULL;',
      '    display_info.user_data = &rgb_info;',
      '',
      '    bsp_display_new_st7701(&display_if, &display_info);',
      '    display_if->init();',
    ].join('\n');

    flushBlock = [
      'static void disp_flush(lv_disp_drv_t *drv, const lv_area_t *area, lv_color_t *color_p) {',
      '    bsp_display_interface_t *di = (bsp_display_interface_t *)drv->user_data;',
      '    bsp_display_area_t da = { .x1 = area->x1, .x2 = area->x2, .y1 = area->y1, .y2 = area->y2 };',
      '    di->flush_dma(&da, color_p);',
      '    lv_disp_flush_ready(drv);',
      '}',
    ].join('\n');
  } else {
    displayInitBlock = [
      '    /* TODO: Initialize your SPI/parallel display */',
      '    /* Example: spi_init(spi1, 62500 * 1000); */',
      '    /* Example: gpio_set_function(display_dc_pin, GPIO_FUNC_SPI); */',
      '    /* Example: ili9341_init(W, H); */',
    ].join('\n');

    flushBlock = [
      'static void disp_flush(lv_disp_drv_t *drv, const lv_area_t *area, lv_color_t *color_p) {',
      '    /* TODO: Send pixel data to your display */',
      '    /* set_window(area->x1, area->y1, area->x2, area->y2); */',
      '    /* write_data((uint8_t *)color_p, w * h * 2); */',
      '    lv_disp_flush_ready(drv);',
      '}',
    ].join('\n');
  }

  return [
    '#if 1',
    '',
    '#include "lv_port_disp.h"',
    '#include <stdbool.h>',
    '#include <stdlib.h>',
    ...(d.type === 'rgb' ? ['#include "pio_rgb.h"', `#include "bsp_${d.controller.toLowerCase()}.h"`] : []),
    '',
    `#define MY_DISP_HOR_RES (${W})`,
    `#define MY_DISP_VER_RES (${H})`,
    '',
    ...(d.type === 'rgb' ? [
      'bsp_display_interface_t *display_if;',
      `static lv_color_t *buf_1;`,
      `static lv_disp_drv_t disp_drv;`,
    ] : [
      `static lv_color_t buf_1[MY_DISP_HOR_RES * 30];`,
      `static lv_disp_drv_t disp_drv;`,
    ]),
    '',
    flushBlock,
    '',
    'void lv_port_disp_init(void) {',
    ...(d.type === 'rgb' ? [displayInitBlock] : [
      displayInitBlock,
    ]),
    '',
    '    static lv_disp_draw_buf_t draw_buf_dsc;',
    ...(d.type === 'rgb' ? [
     `    buf_1 = malloc(MY_DISP_HOR_RES * 10 * sizeof(lv_color_t));`,
     `    lv_disp_draw_buf_init(&draw_buf_dsc, buf_1, NULL, MY_DISP_HOR_RES * 10);`,
    ] : [
     `    lv_disp_draw_buf_init(&draw_buf_dsc, buf_1, NULL, MY_DISP_HOR_RES * 30);`,
    ]),
    '',
    '    lv_disp_drv_init(&disp_drv);',
    '    disp_drv.hor_res = MY_DISP_HOR_RES;',
    '    disp_drv.ver_res = MY_DISP_VER_RES;',
    '    disp_drv.flush_cb = disp_flush;',
    '    disp_drv.draw_buf = &draw_buf_dsc;',
    ...(d.type === 'rgb' ? ['    disp_drv.user_data = display_if;'] : []),
    '    lv_disp_drv_register(&disp_drv);',
    '}',
    '',
    '#endif /*LV_PORT_DISP_H*/',
  ].join('\n');
}

