import JSZip from 'jszip';
import type { LvglWidgetNode } from '../types/lvgl';
import { generateCode } from './index';
import { BOARDS, type BoardDefinition } from './boards';

export function generatePicoProject(
  widgets: LvglWidgetNode[],
  screenW: number,
  screenH: number,
  boardId: string,
  useFreeRtos: boolean = false
): JSZip {
  const board = BOARDS.find(b => b.id === boardId) || BOARDS[0];
  const zip = new JSZip();
  const uiCode = generateCode(widgets, screenW, screenH);

  zip.file('CMakeLists.txt', generateCMake(board, useFreeRtos));
  zip.file('main.c', generateMainC(uiCode, board, screenW, screenH, useFreeRtos));
  zip.file('lv_conf.h', generateLvConf(board));
  zip.file('pico_sdk_import.cmake', picoSdkImportCmake);
  zip.file(`boards/${board.boardHeaderFile}`, board.boardHeaderContent);
  zip.file('port/lv_port_disp.c', generatePortDisp(board));
  zip.file('port/lv_port_disp.h', portDispHeader);
  zip.file('port/lv_port_indev.c', generatePortIndev(board));
  zip.file('port/lv_port_indev.h', portIndevHeader);
  zip.file('.vscode/launch.json', generateLaunchJson(board));
  zip.file('.vscode/settings.json', vscodeSettings);

  if (useFreeRtos) {
    zip.file('FreeRTOSConfig.h', generateFreeRtosConfig(board));
  }

  return zip;
}

/* ============================================================ */
/* CMakeLists.txt                                                */
/* ============================================================ */
function generateCMake(board: BoardDefinition, useFreeRtos: boolean = false): string {
  const cl = board.chip;
  return [
    'cmake_minimum_required(VERSION 3.20)',
    `project(lvgl_${cl})`,
    '',
    'include(pico_sdk_import.cmake)',
    'pico_sdk_init()',
    '',
    '# Board definition',
    `set(PICO_BOARD ${board.id})`,
    'set(PICO_BOARD_HEADER_DIRS ${CMAKE_CURRENT_SOURCE_DIR}/boards)',
    '',
    '# LVGL config',
    'add_compile_options(-DLV_CONF_INCLUDE_SIMPLE)',
    'add_compile_options(-DLV_LVGL_H_INCLUDE_SIMPLE)',
    '',
    '# Create executable',
    `add_executable(lvgl_${cl}`,
    '    main.c',
    '    port/lv_port_disp.c',
    '    port/lv_port_indev.c',
    ')',
    '',
    '# Link Pico SDK libraries',
    `target_link_libraries(lvgl_${cl}`,
    '    pico_stdlib',
    '    hardware_i2c',
    '    hardware_spi',
    '    hardware_pwm',
    '    hardware_dma',
    '    hardware_pio',
    '    hardware_irq',
    '    hardware_gpio',
    '    pico_multicore',
    '    pico_time',
    ')',
    '',
    '# Link BSP libraries',
    ...(board.bspLibs.length > 0 ? board.bspLibs.map(l => `target_link_libraries(lvgl_${cl} ${l})`) : ['# No BSP libraries specified']),
    '',
    '# Add LVGL',
    'add_subdirectory(lvgl)',
    '',
    '# Include directories',
    `target_include_directories(lvgl_${cl} PRIVATE`,
    '    ${CMAKE_CURRENT_SOURCE_DIR}',
    '    ${CMAKE_CURRENT_SOURCE_DIR}/lvgl',
    '    ${CMAKE_CURRENT_SOURCE_DIR}/port',
    '    ${CMAKE_CURRENT_SOURCE_DIR}/boards',
    ...(board.bspHeaders.length > 0 ? ['    ${CMAKE_CURRENT_SOURCE_DIR}/bsp/include'] : []),
    ')',
    '',
    ...(useFreeRtos ? [
      '',
      '# FreeRTOS',
      'add_compile_options(-DUSE_FREERTOS)',
      'add_subdirectory(FreeRTOS_Kernel)',
      `target_include_directories(lvgl_${cl} PRIVATE`,
      '    ${CMAKE_CURRENT_SOURCE_DIR}/FreeRTOS_Kernel/include',
      '    ${CMAKE_CURRENT_SOURCE_DIR}/FreeRTOS_Kernel/portable/ThirdParty/GCC/RP2350_ARM_NTZ/non_secure',
      ')',
      `target_link_libraries(lvgl_${cl} FreeRTOS_Kernel pico_multicore)`,
    ] : []),
    '# Output',
    `set_target_properties(lvgl_${cl} PROPERTIES OUTPUT_NAME "lvgl_${cl}")`,
    `pico_add_extra_outputs(lvgl_${cl})`,
    `pico_enable_stdio_uart(lvgl_${cl} 1)`,
    `pico_enable_stdio_usb(lvgl_${cl} 0)`,
    '',
  ].join('\n');
}

/* ============================================================ */
/* main.c                                                        */
/* ============================================================ */
function generateMainC(uiCode: string, board: BoardDefinition, _sw: number, _sh: number, useFreeRtos: boolean = false): string {
  const uiLines = uiCode.split('\n').filter(l => !l.startsWith('#include "lvgl.h"'));
  const uiFiltered = uiLines.join('\n');
  const isRgb = board.display.type === 'rgb';

  if (useFreeRtos) {
    return [
      '/*',
      ` * LVGL UI for ${board.name} (FreeRTOS)`,
      ` * Display: ${board.resolution.w}x${board.resolution.h}`,
      ' * Generated by LVGL Web Editor',
      ' */',
      '',
      '#include <stdio.h>',
      '#include "pico/stdlib.h"',
      '#include "hardware/i2c.h"',
      '#include "lvgl.h"',
      '#include "FreeRTOS.h"',
      '#include "task.h"',
      '',
      '#include "port/lv_port_disp.h"',
      '#include "port/lv_port_indev.h"',
      '',
      ...(isRgb ? ['#include "bsp_i2c.h"'] : []),
      '',
      `#define LVGL_TICK_PERIOD_MS 10`,
      `#define LVGL_TASK_STACK_SIZE 4096`,
      `#define LVGL_TASK_PRIORITY (tskIDLE_PRIORITY + 2)`,
      '',
      `static void set_cpu_clock(void) {`,
      `    set_sys_clock_khz(${board.cpuClock} * 1000, true);`,
      `    clock_configure(clk_peri, 0,`,
      `        CLOCKS_CLK_PERI_CTRL_AUXSRC_VALUE_CLKSRC_PLL_SYS,`,
      `        ${board.cpuClock} * 1000 * 1000,`,
      `        ${board.cpuClock} * 1000 * 1000);`,
      `}`,
      '',
      'static void lvgl_task(void *arg) {',
      '    TickType_t last_wake = xTaskGetTickCount();',
      '    while (true) {',
      '        lv_timer_handler();',
      '        vTaskDelayUntil(&last_wake, pdMS_TO_TICKS(LVGL_TICK_PERIOD_MS));',
      '    }',
      '}',
      '',
      'int main() {',
      '    set_cpu_clock();',
      '    stdio_init_all();',
      ...(isRgb ? ['    bsp_i2c_init();'] : []),
      '    lv_init();',
      '    lv_port_disp_init();',
      '    lv_port_indev_init();',
      '',
      uiFiltered,
      '',
      '    xTaskCreate(lvgl_task, "lvgl", LVGL_TASK_STACK_SIZE, NULL, LVGL_TASK_PRIORITY, NULL);',
      '    vTaskStartScheduler();',
      '    return 0;',
      '}',
    ].join('\n');
  }

  return [
    '/*',
    ` * LVGL UI for ${board.name}`,
    ` * Display: ${board.resolution.w}x${board.resolution.h}`,
    ' * Generated by LVGL Web Editor',
    ' */',
    '',
    '#include <stdio.h>',
    '#include "pico/stdlib.h"',
    '#include "hardware/i2c.h"',
    '#include "lvgl.h"',
    '',
    '#include "port/lv_port_disp.h"',
    '#include "port/lv_port_indev.h"',
    '',
    ...(isRgb ? ['#include "bsp_i2c.h"'] : []),
    '',
    `#define LVGL_TICK_PERIOD_MS 10`,
    '',
    `static void set_cpu_clock(void) {`,
    `    set_sys_clock_khz(${board.cpuClock} * 1000, true);`,
    `    clock_configure(clk_peri, 0,`,
    `        CLOCKS_CLK_PERI_CTRL_AUXSRC_VALUE_CLKSRC_PLL_SYS,`,
    `        ${board.cpuClock} * 1000 * 1000,`,
    `        ${board.cpuClock} * 1000 * 1000);`,
    `}`,
    '',
    'static bool lvgl_tick_cb(struct repeating_timer *t) {',
    '    lv_tick_inc(LVGL_TICK_PERIOD_MS);',
    '    return true;',
    '}',
    '',
    'int main() {',
    '    static struct repeating_timer lvgl_timer;',
    '    set_cpu_clock();',
    '    stdio_init_all();',
    ...(isRgb ? ['    bsp_i2c_init();'] : []),
    '    lv_init();',
    '    lv_port_disp_init();',
    '    lv_port_indev_init();',
    '    add_repeating_timer_ms(LVGL_TICK_PERIOD_MS, lvgl_tick_cb, NULL, &lvgl_timer);',
    '',
    uiFiltered,
    '',
    '    while (true) {',
    '        lv_timer_handler();',
    '        sleep_ms(LVGL_TICK_PERIOD_MS);',
    '    }',
    '    return 0;',
    '}',
  ].join('\n');
}

