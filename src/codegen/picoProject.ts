/* lv_port_indev.c                                               */
/* ============================================================ */
function generatePortIndev(board: BoardDefinition): string {
  const res = board.resolution;

  const isGt911 = board.touch.controller === 'GT911';
  const touchHeader = isGt911 ? 'bsp_gt911.h' : 'bsp_touch.h';
  const touchNewFn = isGt911
    ? 'bsp_touch_new_gt911(&touch_if, &touch_info)'
    : '/* TODO: Initialize your touch controller */';

  return [
    '#if 1',
    '',
    '#include "lv_port_indev.h"',
    '#include "lvgl.h"',
    `#include "${touchHeader}"`,
    '',
    'static lv_indev_t *indev_touchpad;',
    '',
    'static void touchpad_read(lv_indev_drv_t *drv, lv_indev_data_t *data) {',
    '    static lv_coord_t last_x = 0, last_y = 0;',
    ...(isGt911 ? [
      '    bsp_touch_data_t touch_data;',
      '    bsp_touch_interface_t *touch_if = (bsp_touch_interface_t *)drv->user_data;',
      '    touch_if->read();',
      '    if (touch_if->get_data(&touch_data)) {',
      '        data->point.x = touch_data.x;',
      '        data->point.y = touch_data.y;',
      '        data->state = LV_INDEV_STATE_PRESSED;',
      '        last_x = touch_data.x;',
      '        last_y = touch_data.y;',
      '    } else {',
      '        data->point.x = last_x;',
      '        data->point.y = last_y;',
      '        data->state = LV_INDEV_STATE_RELEASED;',
      '    }',
    ] : [
      '    /* TODO: Read touch coordinates */',
      '    data->state = LV_INDEV_STATE_RELEASED;',
    ]),
    '}',
    '',
    'void lv_port_indev_init(void) {',
    '    static lv_indev_drv_t indev_drv;',
    '',
    ...(isGt911 ? [
      '    bsp_touch_interface_t *touch_if;',
      '    bsp_touch_info_t touch_info;',
     `    touch_info.width = ${res.w};`,
     `    touch_info.height = ${res.h};`,
      '    touch_info.rotation = 0;',
      `    ${touchNewFn};`,
      '    touch_if->init();',
    ] : [
      '    /* TODO: Initialize touch controller */',
    ]),
    '',
    '    lv_indev_drv_init(&indev_drv);',
    '    indev_drv.type = LV_INDEV_TYPE_POINTER;',
    '    indev_drv.read_cb = touchpad_read;',
    ...(isGt911 ? ['    indev_drv.user_data = touch_if;'] : []),
    '    indev_touchpad = lv_indev_drv_register(&indev_drv);',
    '}',
    '',
    '#endif',
  ].join('\n');
}

